                <h1>First Aid</h1> <br/>

<h4> The following is a step by step approach as guidance should you be the first on scene at an accident:</h4>
                
                <ol>
                    <li>When approaching an accident scene it is really important to ensure your own safety.
                    Make sure that all traffic has stopped and it is flagged up that there has been an
                    accident otherwise there may be additional casualties. Be aware of oncoming traffic
                    to ensure that is not posing an additional danger. Note if there is any fuel spillage
                    or potential fire risk; turn off car ignitions if possible.
                    Put on vehicle hazard lights and use a warning triangle if there is one available.</li>

                    <li>If other people are around; get them to phone the emergency services;
                    if you are on your own, assess the situation and treat any life threatening conditions first.</li>

                    <li>Quickly establish how many vehicles have been involved and assess the occupants of
                    all the vehicles to ensure no one has life threatening injuries.</li>

                    <li>People screaming, crying and making a noise have to be breathing;
                    your initial priority is therefore to check anyone quiet and not moving.</li>

                    <li>Quickly check if quiet casualties are responsive: if there is no response
                    check if they are breathing. If they are unresponsive and breathing ensure
                    they are in a position where they are leaning forward or to one side in a
                    position where the airway will remain open. Move them the minimum necessary
                    and avoid twisting them. Keep talking to the casualty calmly as they can hear
                    you even if they are unconscious. Keep them warm.
                  </li>

                  <li>If the person is not breathing you will need to resuscitate;
                  if you are on your own and have not called an ambulance;
                  do this now and ask their advice as to the best way to resuscitate,
                  as this is not easy to do in a car.
                  </li>
                  
                  <li>Only remove an unconscious person from a vehicle if there is an 
                  immediate danger to their life from fire, flood, and explosio. 
                  ask the emergency services over the phone for their advice as to 
                  what you should do. It is very difficult to remove an unconscious
                  person from a vehicle and there is a major danger that you could
                  worsen their injuries and injure yourself in the process.</li>

                  <li>Conscious casualties should be entrusted to the care
                  of bystanders and removed from the wreckage to a safe area.
                  Be aware of confused and dazed casualties who may wander into danger.
                  Brief the bystanders to keep the casualties warm and calm and help them
                  to contact next of kin. Look for any major bleeding and life threatening
                  injuries and treat these first.</li>

                  <li>Note the nature of the wreckage and be aware of possible
                  injuries as a result: bodies are softer than metalwork, so
                  if there is major damage to the vehicle it is possible that
                  there could be internal injuries to the casualty; ensure the
                  bystanders notify you if there is any change in the casualty's condition.
                </li>

                <li>People thrown from horses, motorbikes or bicycles are at particular
                risk of damaging their pelvis. If you know how to stabilise someone's pelvis then do
                this and keep monitoring them for signs of shock as a fractured pelvis can lead to
                internal bleeding.</li>

                <li>Anyone trapped in a vehicle should be monitored carefully and the emergency
                services notified immediately. If someone is crushed, note the exact time when the
                accident happened as this is important in deciding on how and when to release the
                casualty). If there are additional people around, show them how to support the
                person's neck to avoid them twisting as there is the possibility of a spinal injury.</li>

                <li>If there is severe bleeding this will need to be controlled; wear gloves and apply dressings.</li>

                <li>Do not allow anyone to smoke at the scene or give the casualties anything to eat or drink
                following the accident in case they later need an operation.</li>


                </ol>
